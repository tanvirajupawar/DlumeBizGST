// ── Search function ────────────────────────────────────────────────────────────
// Returns { chapter: string, results: [{code, desc, gst}][] }[]
export function searchHSN(query) {
  if (!query || query.trim().length < 2) return [];
  const q = query.trim().toLowerCase();

  const matched = HSN_MASTER.filter(h =>
    h.desc.toLowerCase().includes(q) ||
    h.chapter.toLowerCase().includes(q) ||
    h.code.startsWith(q.replace(/\s/g, ""))
  );

  // Group by chapter
  const grouped = {};
  matched.forEach(h => {
    if (!grouped[h.chapter]) grouped[h.chapter] = [];
    grouped[h.chapter].push(h);
  });

  return Object.entries(grouped).map(([chapter, items]) => ({ chapter, items }));
}




